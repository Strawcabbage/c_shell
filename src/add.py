# Function to add two numbers
def add_two_numbers():
    # Prompt the user for input
    num1 = float(input("Enter the first number: \n"))
    num2 = float(input("Enter the second number: \n"))

    # Calculate the sum
    total = num1 + num2

    # Print the result
    print(f"The sum of {num1} and {num2} is {total}")

# Call the function
add_two_numbers()

